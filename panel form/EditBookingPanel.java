    public EditBookingPanel(Booking booking, ArrayList<Ticket> ticketL) {
        initComponents();
        this.ticketL = ticketL;
        jtfBookingID.setText(booking.getBookingID());
        jtfCustID.setText(booking.getCustomer().getCustID());
        jtfCustName.setText(booking.getCustomer().getCustName());
        jtfDest.setText(ticketL.get(0).getSchedule().getRoute().getDestination());
        jtfDepartDate.setText(ticketL.get(0).getSchedule().getDepartureDate());
        jtfDepartTime.setText(ticketL.get(0).getSchedule().getDepartureTime());
        
        DefaultTableModel model = (DefaultTableModel) ticketTable.getModel();
        for(int i=0; i<ticketL.size(); i++) {           
            model.addRow(new Object[]{ticketL.get(i).getTicketID(), ticketL.get(i).getSeatNo(), ticketL.get(i).getPassengerName(), ticketL.get(i).getPassengerContactNo(), ticketL.get(i).getStatus(), false});
        }
        
        if(booking.getCustomer().getStatus()==' ') {
            jlInfo.setText("The customer is not a member. Cannot change ticket.");
            jlInfo.setForeground(Color.red);
            jbtChange.setEnabled(false);
        } else {
            jlInfo.setText("The customer is a member. Can change ticket.");
            jlInfo.setForeground(Color.blue);
            jbtChange.setEnabled(true);
        }
        
    }

