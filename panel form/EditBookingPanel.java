    private void jbtChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtChangeActionPerformed
        // TODO add your handling code here:
        int count = 0;
        for(int i=0; i<ticketL.size(); i++) {
            if((Boolean)ticketTable.getModel().getValueAt(ticketTable.convertRowIndexToModel(i), 5)) {
                count++;
            }   
        }
        if(count>0) {
            jlError.setText(" ");
        } else {
            jlError.setText("Please select one or more ticket for ticket changing.");
            jlError.setForeground(Color.red);
        }
    }//GEN-LAST:event_jbtChangeActionPerformed


